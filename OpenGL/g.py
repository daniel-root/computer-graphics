from OpenGL.GL import *
from OpenGL.GLUT import *
from OpenGL.GLU import *
from math import *
def SolidCirculo(raio,cx,cy):
    glLineWidth(2)
    glBegin(GL_POLYGON)
    for i in range(0,360):
        ang = (i*pi)/180.0
        x = cx + (cos(ang)*raio)
        y = cy + (sin(ang)*raio)
        glVertex2f(x,y)
    glEnd()
